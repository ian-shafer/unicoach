#!/usr/bin/env python3
import os
import glob
import textwrap

base_dir = os.path.abspath(os.path.join(os.path.dirname(__file__), '..', '.claude', 'skills'))

def compile_skill(output_name, title, desc, glob_pattern):
    output_path = os.path.join(base_dir, output_name, 'SKILL.md')
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    
    content = textwrap.dedent(f"""\
        ---
        name: {output_name}
        description: {desc}
        ---

        # {title}

        > [!WARNING]
        > DO NOT EDIT THIS FILE MANUALLY.
        > This file is auto-generated by `bin/compile-skills.py` from the individual `{output_name.split("-")[0]}-review-*` micro-skills.
        > To update a rule, modify the `implementation_summary` in the corresponding micro-skill.

    """)
    
    files = glob.glob(os.path.join(base_dir, glob_pattern, 'SKILL.md'))
    files.sort()
    
    summaries = []
    
    for f in files:
        with open(f, 'r') as file:
            data = file.read()
            parts = data.split('---', 2)
            if len(parts) >= 3:
                frontmatter = parts[1]
                summary_lines = []
                in_summary = False
                for line in frontmatter.splitlines():
                    if line.startswith('implementation_summary:'):
                        in_summary = True
                        val = line.split(':', 1)[1].strip()
                        if val and val not in ('>', '|', '>-', '|-'):
                            summary_lines.append(val)
                            in_summary = False
                        continue
                    if in_summary:
                        if line.startswith('  '):
                            summary_lines.append(line.strip())
                        elif line.strip() == '':
                            summary_lines.append('')
                        else:
                            in_summary = False
                
                if summary_lines:
                    summary_text = ' '.join(l for l in summary_lines if l).strip()
                    summaries.append(f"- {summary_text}")
    
    if summaries:
        content += "\n".join(summaries) + "\n"
        
    with open(output_path, 'w') as f:
        f.write(content)
        
    print(f"Compiled {len(summaries)} skills into {output_name}/SKILL.md")

if __name__ == "__main__":
    compile_skill(
        output_name="coding",
        title="🤖 Skill: Defensive Coding (Implementation Only)",
        desc="Global baseline philosophies for programming safety and data integrity.",
        glob_pattern="code-review-*"
    )
    compile_skill(
        output_name="general-design",
        title="General Code and Design (Implementation Only)",
        desc="Opinionated philosophical constraints to apply when writing code.",
        glob_pattern="design-review-*"
    )
